    /**
     * 
     *
     * @param oldDouble
     * @param scale
     * @return
     */
    public static String significand(String number, int scale) {
        if (scale < 0) {
            throw new IllegalArgumentException(
                    "scale");
        }
        /**
         * RoundingMode：
         * UP：；
         * DOWN：；
         * CEILING： ；
         * FLOOR：；
         * HALF_DOWN：，，；
         * HALF_UP：，，；
         * HALF_EVEN：，，;(,)
         * UNNECESSARY：，。
         */
        RoundingMode rMode = null;
        //rMode=RoundingMode.FLOOR;
        //，FLOOR。
        if (new BigDecimal(number).compareTo(new BigDecimal("0")) == 1) {
            rMode = RoundingMode.DOWN;
        } else {
            rMode = RoundingMode.UP;
        }
        //scale，
        BigDecimal b = new BigDecimal(number, new MathContext(scale, rMode));
        return b.toString();
    }
